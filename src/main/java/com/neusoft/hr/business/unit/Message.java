package com.neusoft.hr.business.unit;

public class Message {
    private int mesId;
    private String mesName;

    public Message() {
    }

    public int getMesId() {
        return mesId;
    }

    public void setMesId(int mesId) {
        this.mesId = mesId;
    }

    public String getMesName() {
        return mesName;
    }

    public void setMesName(String mesName) {
        this.mesName = mesName;
    }
}
